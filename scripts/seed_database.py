def slugify(text):
    """Convert text to a URL-friendly slug."""
    text = text.lower()
    text = re.sub(r"[^\w\s-]", "", text)
    text = re.sub(r"[\s_-]+", "-", text)
    return text.strip("-")


def create_schema(conn):
    conn.execute("""
        CREATE TABLE IF NOT EXISTS strategies (
            id INTEGER PRIMARY KEY,
            organism TEXT NOT NULL,
            organism_scientific TEXT,
            kingdom TEXT,
            habitat TEXT,
            biological_function TEXT,
            biomimicry_taxonomy_group TEXT,
            biomimicry_taxonomy_subgroup TEXT,
            human_application TEXT,
            real_world_products TEXT,
            industry_tags TEXT,
            key_principle TEXT,
            source_url TEXT,
            slug TEXT UNIQUE NOT NULL
        )
    """)
    conn.commit()


