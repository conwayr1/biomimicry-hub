def insert_strategies(conn):
    cursor = conn.cursor()
    inserted = 0
    skipped = 0
    for s in STRATEGIES:
        # Auto-generate slug if not provided
        if not s.get("slug"):
            s["slug"] = slugify(s["organism"])
        try:
            cursor.execute("""
                INSERT INTO strategies (
                    organism, organism_scientific, kingdom, habitat,
                    biological_function, biomimicry_taxonomy_group,
                    biomimicry_taxonomy_subgroup, human_application,
                    real_world_products, industry_tags, key_principle,
                    source_url, slug
                ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
            """, (
                s["organism"], s.get("organism_scientific"), s.get("kingdom"),
                s.get("habitat"), s.get("biological_function"),
                s.get("biomimicry_taxonomy_group"), s.get("biomimicry_taxonomy_subgroup"),
                s.get("human_application"), s.get("real_world_products"),
                s.get("industry_tags"), s.get("key_principle"),
                s.get("source_url"), s["slug"]
            ))
            inserted += 1
        except sqlite3.IntegrityError:
            skipped += 1  # Duplicate slug — already in DB
    conn.commit()
    return inserted, skipped


def main():
    os.makedirs(os.path.dirname(DB_PATH), exist_ok=True)
    conn = sqlite3.connect(DB_PATH)
    create_schema(conn)
    inserted, skipped = insert_strategies(conn)
    conn.close()
    print(f"Done. Inserted {inserted} strategies, skipped {skipped} duplicates.")
    print(f"Database saved to: {os.path.abspath(DB_PATH)}")


if __name__ == "__main__":
    main()
